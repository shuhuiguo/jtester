package fitlibrary.utility;

public class MapElement {
	private Object key, value;

	public MapElement(Object key, Object value) {
		this.key = key;
		this.value = value;
	}

	public Object getKey() {
		return key;
	}

	public Object getValue() {
		return value;
	}
}
